        <h1>Trip Overview</h1>
        <p>The Everest Base Camp trek passes through the land of the Sherpas to the foot of the 
            world’s highest mountain, Sagarmatha, Nepalese for Mount Everest. The fullest views of Mt.
             Everest actually are from the Tibetan side, but this trek is about so much more than one mountain.
              Multiple 8,000-meter peaks line the Khumbu region, the grandest stage of all for alpine trekking. 
              You’ll have to stomach the hair-raising flight into Lukla and this is one of Nepal’s most popular trekking routes, 
              but the mountain views are simply unmatched anywhere else in the world.<br>

              Everest Base Camp treks are some of the best treks in the world, and the hike to Everest Base
               Camp is possible through different routes. This is the classic and most popular hike to Everest 
               Base Camp. Though there are several Everest Camps, this is the trek to the Everest Base Camp. There are 
               more Everest Camps above Everest Base Camp. Mount Everest Camps provide resting places for climbers aiming 
               to conquer Mount Everest.
        </p>
        <div class="image">
             <img src="namche.jpg">
        </div>
            
        <p>
       The journey beings with the flight from Kathmandu to the hair-raising Lukla airstrip, perched at a lofty 2,800 meters (9,200 feet). 
       The small airstrip frequently makes lists of the world’s most dangerous airports and there have been numerous crashes over the years.
        But you likely are safer making the journey by air than by Nepal’s system of mountain roads with sheep drops into steep canyons, no guardrails,
         and buses that could be described as “aging” at best. Altitude is an important consideration on the Everest Base Camp trek and nearby Namche Bazar (3,440 m; 11,286 ft),
        the Sherpa village and gateway to the Khumbu region, is often used as an early layover for rest and acclimatization.
        <br> 
        From Namche the trek heads upward through colorful Sherpa villages along the Dudh Kosi River. Terraced rice fields and rhododendron forests 
        eventually turn into hardened landscapes of rock and ice. On the approach to Everest Base Camp is the final outpost of Gorak Shep; there are no 
        teahouses at Everest Base Camp so trekkers can either day hike from Gorak Shep or bring camping gear. The scramble to nearby Kala Patthar (5,545 m; 18,192 ft) offers
         a panorama of Everest, Lhotse, and Nuptse that bests the views from base camp itself. The main Everest Base Camp trail is an up-and-back route, meaning that you hike down the same trail you hiked up.<br>

        Everest Base Camp is Nepal’s most popular trek with upward of 30,000 trekkers per year and 10,000 per month during peak season 
        (these numbers are from before the 2015 earthquake and reliable information since then unfortunately is not available). 
        An upside to the number of trekkers is a well worn trail and facilities are as good as anywhere in Nepal. For those 
        seeking to get off the beaten path, side trips up the Gokyo Valley and Chukhung Valley will greatly enhance the trip. 
       
        </p>
