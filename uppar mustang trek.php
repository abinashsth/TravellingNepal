    <div class="trip">
        <h1>Trip Overview</h1>
        <p>Upper Mustang Trek is located in the ancient Himalayan Kingdom which lies in the northern central part of Nepal and at
            the top of the Kaligandaki River. When you enter the restricted area, you enter an ancient land with its own distinct
            history and tradition. Part of Nepal for two hundred years, Upper Mustang remains one of the few areas in Tibet’s
            original sphere of influence where Tibetan culture continues to survive. The challenge we face today is to improve our
            living conditions, and at the same time to strengthen and keep alive our unique culture.
            Mustang was a forbidden land for centuries and little was known of this Trans Himalayan valley which has
            a wonderful variety of its heritage, lively Tibetan culture, and fascinating landscape. Early visitors explored
            the silent features of this unique hidden land, its great castle, ancient monasteries, lonely hermitage, and remote villages.
        </p>

        <div class="image">
            <img src="manag.jpg">
        </div>



        <p> The Upper Mustang trek winds its way to the hidden Buddhist world of the Kingdom of Mustang, also known as Lo. This remote
            trans-Himalayan mountain area borders Tibet and has a distinct Tibetan feel, both in its people and culture and in its arid
            landscape of high cliffs and rocks of unusual formations and colour.The trek visits Kagbeni, the gateway to Upper Mustang,
            then through a stark landscape in the rain shadow of the Dhaulagiri massif to Lo-Manthang, the seat of past Kings of the
            Kingdom of Lo.
            Explore caves, rock paintings, monasteries, and gompa and learn something about the culture of this area. Panoramic views of
            Nilgiri, Annapurna, Dhaulagiri, and others, are very much part of this exciting trip!
            Within Lo Manthang itself, a visit to Namgyal Gompa and Tingkhar, the last main village in the northwest of the area, as
            well as the King’s palace, brings a further cultural understanding of the area.There is also an amchi (traditional Tibetan doctor)
            museum and school where visitors can learn about this ancient healing art and how it is being adapted to meet modern-day situations.</p>
        <div class="image">
            <img src="Mustang 1.jpg" height="800px" width="80%">
        </div>
        <p>A few hours’ walk from Lo Manthang is Gyakar, with its famous Gompa. Ghar Gompa is over 1,200 years old, is dedicated to Guru
            Rinpoche, who traveled these areas then.Gompa is famous for its rock paintings and the fact it is believed that if you make a
            wish here, it will come true. So make a wish at the Ghar Gompa, and hope to return again someday!The trek is at a relatively
            high altitude (3,000m to 3,800m), intensified by the dry air.While it is no problem for those born at this altitude to travel
            at speed, visitors will be more leisurely in their hike to avoid any altitude-related problems. On average, walking takes 5 to 7
            hours a day, and some paths are particularly windy and dusty.But this trek into the restricted area of Upper Mustang, which
            in part follows the ancient salt route, will remain in one’s memory forever.
        </p>
        <h1>Why The Upper Mustang Trek?</h1>
        <p>Trekking in the Upper Mustang is a special privilege. Here you will mee the lifestyles of true mountain people,
            who, for lots of years, had very little contact with the rest of Nepal and managed to retain their wealthy cultural
            heritage. Until recently, their king was formally recognized by the Government of Nepal.

            In many ways, a trek into Upper Mustang is comparable to trekking in Tibet, as geographically
            it is a part of the Tibetan plateau. The district of Mustang was, till 1950, a separate kingdom inside the
            boundaries of Nepal. The remaining king, the Raja of Mustang, still resides in the historical capital recognized
            as Lo Manthang.
        </p>

